export type Locale = keyof typeof localization;
export type TranslationKey = keyof (typeof localization)[typeof DEFAULT_LOCALE];

export function t(
  key: TranslationKey,
  locale: Locale = DEFAULT_LOCALE,
  params?: Record<string, string | number>,
): string {
  const template: string =
    localization[locale][key] ?? localization[DEFAULT_LOCALE][key] ?? key;

  if (!params) {
    return template;
  }

  return Object.entries(params).reduce(
    (message, [paramKey, value]) =>
      message.split(`{${paramKey}}`).join(String(value)),
    template,
  );
}
